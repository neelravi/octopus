      subroutine pgl(a,b,r,nc,ib,ihg)
!
!     .. Scalar Arguments ..
      integer :: ihg, nc
!     ..
!     .. Array Arguments ..
      FLOAT :: a(3,3), b(3,3), r(49,3,3)
      integer :: ib(48)
!     ..
!     .. Local Scalars ..
      FLOAT :: eps, tr
      parameter(eps=1.0d-8)    ! used to be 1.0d-8
      integer :: i, ihc, j, k, lx, n, nr
!     ..
!     .. Local Arrays ..
      FLOAT :: vr(3), xa(3)
!     ..
!     .. Intrinsic Functions ..
      intrinsic abs
!     ..
!
!     eps should be slightly larger than computer precision
!
      
      ihc = 0
!
!     ihc is 0 for hexagonal groups and 1 for cubic groups.
!
      nr = 24
   10 continue
      nc = 0
      call rot(r,nr)
      do 60 n = 1, nr
         ib(n) = 0
         tr = 0.0d0
         do 50 k = 1, 3
            do 30 i = 1, 3
               xa(i) = 0.0d0
               do 20 j = 1, 3
                  xa(i) = xa(i) + r(n,i,j)*a(j,k)
   20          continue
   30       continue
            call rlv(b,xa,vr,lx)
            do 40 i = 1, 3
               tr = tr + abs(vr(i))
   40       continue
   50    continue
         if (tr .le. 10.0D0*eps) then
            nc = nc + 1
            ib(nc) = n
         end if
   60 continue
      if (ihc .eq. 0) then
         if (nc .eq. 12) then
            ihg = 6
!
            return
!
         end if
         if (nc .gt. 12) then
            ihg = 7
!
            return
!
         end if
         if (nc .lt. 12) then
            nr = 48
            ihc = 1
!
            go to 10
!
         end if
      else
         if (nc .eq. 16) then
            ihg = 4
!
            return
!
         end if
         if (nc .gt. 16) then
            ihg = 5
!
            return
!
         end if
         if (nc .lt. 16) then
            if (nc .eq. 4) then
               ihg = 2
!
               return
!
            end if
            if (nc .gt. 4) then
               ihg = 3
!
               return
!
            end if
            if (nc .lt. 4) then
               ihg = 1
!
               return
!
            end if
         end if
      end if
!
!     ihg stands for holohedral group number.
!
      end subroutine pgl
!
      subroutine rot(r,nr)
!
!     .. Scalar Arguments ..
      integer :: nr
!     ..
!     .. Array Arguments ..
      FLOAT :: r(49,3,3)
!     ..
!     .. Local Scalars ..
      FLOAT :: f
      integer :: i, j, k, n, nv
!     ..
!     .. Intrinsic Functions ..
      intrinsic sqrt
!     ..
      do 30 n = 1, nr
         do 20 i = 1, 3
            do 10 j = 1, 3
               r(n,i,j) = 0.0d0
   10       continue
   20    continue
   30 continue
!
      if (nr .le. 24) then
!
!        define the generators for the rotation matrices
!                                 --hexagonal group
!
         f = sqrt(3.0d0)/2.0d0
         r(2,1,1) = 0.5d0
         r(2,1,2) = -f
         r(2,2,1) = f
         r(2,2,2) = 0.5d0
         r(7,1,1) = -0.5d0
         r(7,1,2) = -f
         r(7,2,1) = -f
         r(7,2,2) = 0.5d0
         do 40 n = 1, 6
            r(n,3,3) = 1.0d0
            r(n+18,3,3) = 1.0d0
            r(n+6,3,3) = -1.0d0
            r(n+12,3,3) = -1.0d0
   40    continue
!
!     generate the rest of the rotation matrices
!
         do 70 i = 1, 2
            r(1,i,i) = 1.0d0
            do 60 j = 1, 2
               r(6,i,j) = r(2,j,i)
               do 50 k = 1, 2
                  r(3,i,j) = r(3,i,j) + r(2,i,k)*r(2,k,j)
                  r(8,i,j) = r(8,i,j) + r(2,i,k)*r(7,k,j)
                  r(12,i,j) = r(12,i,j) + r(7,i,k)*r(2,k,j)
   50          continue
   60       continue
   70    continue
         do 100 i = 1, 2
            do 90 j = 1, 2
               r(5,i,j) = r(3,j,i)
               do 80 k = 1, 2
                  r(4,i,j) = r(4,i,j) + r(2,i,k)*r(3,k,j)
                  r(9,i,j) = r(9,i,j) + r(2,i,k)*r(8,k,j)
                  r(10,i,j) = r(10,i,j) + r(12,i,k)*r(3,k,j)
                  r(11,i,j) = r(11,i,j) + r(12,i,k)*r(2,k,j)
   80          continue
   90       continue
  100    continue
!
         do 130 n = 1, 12
            nv = n + 12
            do 120 i = 1, 2
               do 110 j = 1, 2
                  r(nv,i,j) = -r(n,i,j)
  110          continue
  120       continue
  130    continue
      else
!
!        define the generators for the rotation matrices
!                                          --cubic group
!
         r(9,1,3) = 1.0d0
         r(9,2,1) = 1.0d0
         r(9,3,2) = 1.0d0
         r(19,1,1) = 1.0d0
         r(19,2,3) = -1.0d0
         r(19,3,2) = 1.0d0
         do 160 i = 1, 3
            r(1,i,i) = 1.0d0
            do 150 j = 1, 3
               r(20,i,j) = r(19,j,i)
               r(5,i,j) = r(9,j,i)
               do 140 k = 1, 3
                  r(2,i,j) = r(2,i,j) + r(19,i,k)*r(19,k,j)
                  r(16,i,j) = r(16,i,j) + r(9,i,k)*r(19,k,j)
                  r(23,i,j) = r(23,i,j) + r(19,i,k)*r(9,k,j)
  140          continue
  150       continue
  160    continue
         do 190 i = 1, 3
            do 180 j = 1, 3
               do 170 k = 1, 3
                  r(6,i,j) = r(6,i,j) + r(2,i,k)*r(5,k,j)
                  r(7,i,j) = r(7,i,j) + r(16,i,k)*r(23,k,j)
                  r(8,i,j) = r(8,i,j) + r(5,i,k)*r(2,k,j)
                  r(10,i,j) = r(10,i,j) + r(2,i,k)*r(9,k,j)
                  r(11,i,j) = r(11,i,j) + r(9,i,k)*r(2,k,j)
                  r(12,i,j) = r(12,i,j) + r(23,i,k)*r(16,k,j)
                  r(14,i,j) = r(14,i,j) + r(16,i,k)*r(2,k,j)
                  r(15,i,j) = r(15,i,j) + r(2,i,k)*r(16,k,j)
                  r(22,i,j) = r(22,i,j) + r(23,i,k)*r(2,k,j)
                  r(24,i,j) = r(24,i,j) + r(2,i,k)*r(23,k,j)
  170          continue
  180       continue
  190    continue
         do 220 i = 1, 3
            do 210 j = 1, 3
               do 200 k = 1, 3
                  r(3,i,j) = r(3,i,j) + r(5,i,k)*r(12,k,j)
                  r(4,i,j) = r(4,i,j) + r(5,i,k)*r(10,k,j)
                  r(13,i,j) = r(13,i,j) + r(23,i,k)*r(11,k,j)
                  r(17,i,j) = r(17,i,j) + r(16,i,k)*r(12,k,j)
                  r(18,i,j) = r(18,i,j) + r(16,i,k)*r(10,k,j)
                  r(21,i,j) = r(21,i,j) + r(12,i,k)*r(15,k,j)
  200          continue
  210       continue
  220    continue
         do 250 n = 1, 24
            nv = n + 24
            do 240 i = 1, 3
               do 230 j = 1, 3
                  r(nv,i,j) = -r(n,i,j)
  230          continue
  240       continue
  250    continue
      end if
!
      end subroutine rot
!     ----------------------------------------------
      subroutine rlv(p,g,y,l)
      
      implicit none
!
!     INPUT:
!     -----
!
      FLOAT  g(3),  &  ! vector to be multiplied
                        p(3,3)    ! multiplication matrix, e.g. lattice vectors
!      
!
!     OUTPUT:
!     ------
!
      FLOAT  y(3)    ! mod(multiplied vector,lattice vector)
      integer l                 ! is nonzero if a lattice vector was removed

!     subroutine rlv removes a direct lattice vector from g by
!     operation p, leaving the remainder in y. If a nonzero lattice
!     vector was removed, l is made nonzero.

      intrinsic abs, nint

      y(1:3)=matmul(p(1:3,1:3),g(1:3))
      l=sum(nint(abs(y(1:3))))
      y(1:3)=y(1:3)-1.d0*nint(y(1:3))
      
      return
!
      end subroutine rlv
